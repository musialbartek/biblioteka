    ob_start();
if (isset($_COOKIE['lastActivity'])) {
    $lastActivity = $_COOKIE['lastActivity'];
    $inactiveTime = time() - $lastActivity;
    $logoutTime = 1800;

    if ($inactiveTime > $logoutTime) {

        unset($_COOKIE['lastActivity']);
        setcookie('lastActivity', '', time() - 3600);
        session_destroy();
        header("Location: login.php");
        exit;
    }
}


